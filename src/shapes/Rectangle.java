public class Rectangle extends Shape {
     private double height=5.0;
    private double Width=6.2;



    public double getHeight() {
        return height;
    }

    public double getWidth() {
        return Width;
    }
    public double getArea (){
        return Width*height;
        
    }
    
    
}
